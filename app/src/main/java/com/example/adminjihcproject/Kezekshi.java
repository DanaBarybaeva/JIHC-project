package com.example.adminjihcproject;

public class Kezekshi {
    String dayk;
    String timek;
    String whok;


    public Kezekshi(String dayk,  String timek,String whok) {
        this.dayk = dayk;
        this.timek = timek;
        this.whok = whok;

    }

    public String getDayk() {
        return dayk;
    }

    public void setDayk(String dayk) {
        this.dayk = dayk;
    }

    public String getWhok() {
        return whok;
    }

    public void setWhok(String whok) {
        this.whok = whok;
    }

    public String getTimek() {
        return timek;
    }

    public void setTimek(String timek) {
        this.timek = timek;
    }
    public Kezekshi(){}


}
